    private void btnAumentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAumentarActionPerformed
        incrementador = Integer.parseInt(tfincrementador.getText());
        contador = contador + incrementador;
        lbContador1.setText(String.valueOf (contador));
        lbContador2.setText(String.valueOf (contador));
                
        System.out.println("Suma: "+contador);
        
    }//GEN-LAST:event_btnAumentarActionPerformed

    private void btnLimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimpiarActionPerformed
        contador = 0;
        lbContador1.setText(String.valueOf (contador));;
        
        lbContador2.setText(String.valueOf (contador));
        System.out.println("Limpiar: "+contador);
    }//GEN-LAST:event_btnLimpiarActionPerformed

    private void btnDisminuir1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisminuir1ActionPerformed
        incrementador = Integer.parseInt(tfincrementador.getText());
        contador = contador - incrementador;
        lbContador1.setText(String.valueOf (contador));;
        
        lbContador2.setText(String.valueOf (contador));
        System.out.println("Resta: "+contador);
        
    }//GEN-LAST:event_btnDisminuir1ActionPerformed

